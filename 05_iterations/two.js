//While and Do while loop
let i = 1
while ( i <= 10 ){
    // console.log(`i = ${i}`);
    i = i + 2
}   

let myArray = ["flash", "batman", "superman"]

let arr = 0
while (arr < myArray.length ){
    // console.log(`Value of myArr[${arr}] is = ${myArray[arr]}`);
    arr = arr + 1
}

let score = 1

do {
    console.log(`Score = ${score}`);
    score++
} while (score <= 15);